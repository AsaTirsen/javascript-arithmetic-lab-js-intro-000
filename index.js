// function for addition
function add(a, b) {
  return a+b;
}

// function for subtraction
function subtract(a, b) {
  return a-b;
}

// function for multiplication
function multiply(a, b) {
  return a*b;
}

// function for multiplication
function divide(a, b) {
  return a/b;
}

//increments n
function inc(n) {
  return ++n;
}

//decrements n
function dec(n) {
  return --n;
}
